/**
 * Definition for singly-linked list.
 *
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        auto cur = head;
        if (head==nullptr || n<0){
            return head;
        }
        while (cur!= nullptr){
            n--;
            cur=cur->next;
        }
        if(n>0){
            return head;
        }else if(n==0){
            head=head->next;
        }else {
            cur = head;

            while (++n!= 0) {
                cur = cur->next;
            }
            cur->next = cur->next->next;

        }
        return head;

    }
};
